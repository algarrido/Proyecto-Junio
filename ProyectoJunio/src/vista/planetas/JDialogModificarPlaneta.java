    private void jButtonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModificarActionPerformed

        if (!jTextFieldNmbre.getText().equals("") && !jTextFieldNucleo.getText().equals("") && !jTextFieldSatelites.getText().equals("") && !jTextFieldDiametro.getText().equals("")) {
            try {
                String nombre = jTextFieldNmbre.getText();
                String nucleo = jTextFieldNucleo.getText();
                int satelites = Integer.parseInt(jTextFieldSatelites.getText());
                double diametro = Double.parseDouble(jTextFieldDiametro.getText());
               
                nuevoP = new Planeta(nombre, nucleo, diametro,satelites,sistema);
                dispose();

            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "Error, no introducir letras en campos num√©ricos.");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Error, rellena todos los campos.");
        }

    }//GEN-LAST:event_jButtonModificarActionPerformed

